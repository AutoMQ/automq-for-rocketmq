package com.automq.rocketmq.controller.metadata.database.dao;

import java.util.Objects;

public class Range {

    long id;

    int rangeId;

    long streamId;

    long epoch;

    long startOffset;

    long endOffset;

    int brokerId;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public int getRangeId() {
        return rangeId;
    }

    public void setRangeId(int rangeId) {
        this.rangeId = rangeId;
    }

    public long getStreamId() {
        return streamId;
    }

    public void setStreamId(long streamId) {
        this.streamId = streamId;
    }

    public long getEpoch() {
        return epoch;
    }

    public void setEpoch(long epoch) {
        this.epoch = epoch;
    }

    public long getStartOffset() {
        return startOffset;
    }

    public void setStartOffset(long startOffset) {
        this.startOffset = startOffset;
    }

    public long getEndOffset() {
        return endOffset;
    }

    public void setEndOffset(long endOffset) {
        this.endOffset = endOffset;
    }

    public int getBrokerId() {
        return brokerId;
    }

    public void setBrokerId(int brokerId) {
        this.brokerId = brokerId;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Range range = (Range) o;
        return id == range.id && rangeId == range.rangeId && streamId == range.streamId && epoch == range.epoch && startOffset == range.startOffset && endOffset == range.endOffset && brokerId == range.brokerId;
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, rangeId, streamId, epoch, startOffset, endOffset, brokerId);
    }
}
